    private void btVincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVincularActionPerformed
        
        compra = new Compra();
        linha = jTPessoa.getSelectedRow();
        linha2 = jTCarro.getSelectedRow();
        
        compra.getPessoa().setNome(jTPessoa.getValueAt(linha, 0).toString());
        compra.getPessoa().setRg(jTPessoa.getValueAt(linha, 1).toString());
        
        compra.getAuto().setPlaca(jTCarro.getValueAt(linha2, 0).toString());
        compra.getAuto().setModelo(jTCarro.getValueAt(linha2, 1).toString());
        
        try {
            arqout3.Adicionar(compra);
        } catch (IOException ex) {
            Logger.getLogger(CompraRealizada.class
                .getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(
            null, "Compra vindulada com sucesso!");
      

        int cont = 1;

        List<Compra> listacompra = new ArrayList<Compra>();

        listacompra= arqin3.getTodasCompras();
        DefaultTableModel modelocompra = (DefaultTableModel) jTCompra.getModel();

        for (int i = jTCompra.getRowCount() - 1;
            i >= 0; --i) {
            modelocompra.removeRow(i);
        }

        for (int i = 0;
            i < listacompra.size();
            i++) {
            modelocompra.addRow(listacompra.get(i).getcompra());
            cont++;
        }
        
    }//GEN-LAST:event_btVincularActionPerformed

