    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        
        DefaultTableModel modelo = (DefaultTableModel) jTCompra.getModel();
        List<Compra> list = new ArrayList<Compra>();
        ArrayList<Compra> nova_list = new ArrayList<Compra>();
        linha = jTCompra.getSelectedRow();
        Object valor = modelo.getValueAt(linha, 1);
        list = arqin3.getTodasCompras();

        for (int i = 0; i < list.size(); i++) {
            if (!(list.get(i).getPessoa().getRg().equals(valor))) {
                nova_list.add(list.get(i));
            }
        }
        try {
            arqout3.recadastrar_todos(nova_list);
        } catch (IOException ex) {
            Logger.getLogger(CompraRealizada.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelo.removeRow(linha);
        JOptionPane.showMessageDialog(null, "Compra apagada com sucesso");
        
    }//GEN-LAST:event_btExcluirActionPerformed

